#include <iostream>
#include <string>
using namespace std;
int main(){
    string s,ans;
    ans="";
    cin>>s;
    if((s[0]+2)<='h'&&(s[0]+2)>='a'){
        if((s[1]+1)<='8'&&(s[1]+1)>='1'){
            ans+=s[0]+2;ans+=s[1]+1;ans+='\n';
        }
        if((s[1]-1)<='8'&&(s[1]-1)>='1'){
            ans+=(s[0]+2);ans+=(s[1]-1);ans+='\n';
        }
    }
    if((s[0]-2)<='h'&&(s[0]-2)>='a'){
        if((s[1]+1)<='8'&&(s[1]+1)>='1'){
            ans+=s[0]-2;ans+=s[1]+1;ans+='\n';
        }
        if((s[1]-1)<='8'&&(s[1]-1)>='1'){
            ans+=(s[0]-2);ans+=(s[1]-1);ans+='\n';
        }
    }
    if((s[0]+1)<='h'&&(s[0]+1)>='a'){
        if((s[1]+2)<='8'&&(s[1]+2)>='1'){
            ans+=s[0]+1;ans+=s[1]+2;ans+='\n';
        }
        if((s[1]-2)<='8'&&(s[1]-2)>='1'){
            ans+=(s[0]+1);ans+=(s[1]-2);ans+='\n';
        }
    }
    if((s[0]-1)<='h'&&(s[0]-1)>='a'){
        if((s[1]+2)<='8'&&(s[1]+2)>='1'){
            ans+=s[0]-1;ans+=s[1]+2;ans+='\n';
        }
        if((s[1]-2)<='8'&&(s[1]-2)>='1'){
            ans+=(s[0]-1);ans+=(s[1]-2);ans+='\n';
        }
    }
    //s+='\n';
    //s+="kek";
    cout<<ans;
    return 0;
}
